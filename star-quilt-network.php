    <h3 class="subheading">About star quilts</h3>
    <p>There are a lot of stories about the origins and significance of star quilts. Star quilts have a rich history, a modern history, and they are an important symbol of survival and expression. Star quilts replaced buffalo robes out of necessity and creativity. Homemade star quilts were a supplement to army blanket rations. The army blankets were even used as materials for making star quilts. In harsh winter climates, and before central heating and housing developments, lots of blankets were a good thing to have.</p>
    <p>Quilting is a Western art form, but it has been adapted to Native American life. Adaptation is a virtue of our culture and our people. History has taught that in the face of extermination, our spirit endures. We live in the physical and mental worlds, but we also live in the spiritual and emotional worlds as well.</p>
  	<p>Today, star quilts are everywhere. They are used as gifts, they are used in ceremonies, they are purchased for the home, and they are commissioned by collectors. Tribal programs, colleges, and community nonprofits also help promote the tradition of giving star quilts as gifts, by making a point to honor co-workers and special guests with star quilts during meetings, conferences, and banquets.  Star quilts have also received National recognition. Presidents and international dignitaries can be seen receiving star quilts in National newspapers and magazines around the world.</p>
    <p>Star quilts are part of the economic landscape in Native American communities. During graduations, star quilts are in high demand. Gift shops and pawn shops buy low throughout the year, and sell high during the peak season.</p>
  	<p>In terms of customer size, anyone in the world needs a blanket. By experimenting with different product types, the star quilt design could be extended to clothing, fabric paints, embroidery, mugs, keychains, and many other types of products where the star quilt design can be adapted.</p>
  	<p>For Native American communities, star quilts can build skills as a type of informal education. The skills for making new star quilts are being taught from generation to generation. Our eductional system and colleges may only reach a percentage of Tribal members every year, but making star quilts reaches everyone. No exam is required to get started, only an aunt, cousin, or friend who knows how to make star quilts is needed.</p>
